package net.sf.dframe.cluster.pojo;

/**
 * 多播
 * @author dy02
 *
 */
public class Multicast {
	
	private String group;
	
	private int port;

	/**
	 * getGroup
	 * @return group
	 */
	public String getGroup() {
		return group;
	}

	/**
	 * setGroup
	 * @param group
	 */
	public void setGroup(String group) {
		this.group = group;
	}

	/**
	 * getPort
	 * @return port
	 */
	public int getPort() {
		return port;
	}

	/**
	 * setPort
	 * @param port
	 */
	public void setPort(int port) {
		this.port = port;
	}
	
	
	
}
